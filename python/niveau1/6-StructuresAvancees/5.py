hauteur = int(input())
nbFolioles = int(input())

if (hauteur <= 5 and nbFolioles >= 8):
    print("Tinuviel")

if (hauteur >= 10 and nbFolioles >= 10):
    print("Calaelen")

if (hauteur <= 8 and nbFolioles <= 5):
    print("Falarion")

if (hauteur >= 12 and nbFolioles <= 7):
    print("Dorthonion")



